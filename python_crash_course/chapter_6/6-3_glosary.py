# 6-3_glosary.py

glosary = {
    'string': 'simply a series of characters.',
    'list': 'is a collection of items in a particular order.',
    'append': 'is a method that adds an item to the list.',
    'tuple': 'is an immutable list.',
    'dictionary': 'is a collection of key-value pairs.',
    }

print("\nSTRING:")
print("\t" + glosary['string'].capitalize())

print("\nLIST:")
print("\t" + glosary['list'].capitalize())

print("\nAPPEND:")
print("\t" + glosary['append'].capitalize())

print("\nTUPLE:")
print("\t" + glosary['tuple'].capitalize())

print("\nDICTIONARY:")
print("\t" + glosary['dictionary'].capitalize() + "\n")
