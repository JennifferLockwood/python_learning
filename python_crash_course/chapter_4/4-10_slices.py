pets = ['fish', 'birds', 'guinea pigs', 'dogs','hamsters','ferrets', 'rabbits']
print("The first three pets in the list are:")
print(pets[0:3])

print("\nThree pets from the middle of the list are:")
print(pets[2:5])

print("\nThe last three pets in the list are:")
print(pets[-3:])
